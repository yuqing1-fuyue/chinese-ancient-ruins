def get_image_url(ruin_name):
    """获取遗址真实图片URL - 优先使用本地图片"""
    
    # 优先返回本地图片URL
    if ruin_name in LOCAL_IMAGE_URLS:
        return LOCAL_IMAGE_URLS[ruin_name]
    
    # 如果没有对应图片，生成一个渐变背景占位图
    color_schemes = [
        ("#8B4513", "#DEB887", "#CD853F"),
        ("#8B0000", "#CD853F", "#B22222"),
        ("#2E8B57", "#90EE90", "#3CB371"),
        ("#556B2F", "#8FBC8F", "#6B8E23"),
        ("#4A4A8A", "#9090CC", "#6666AA"),
        ("#8B7355", "#D2B48C", "#A0876B"),
    ]
    
    color_idx = sum(ord(c) for c in ruin_name) % len(color_schemes)
    c1, c2, c3 = color_schemes[color_idx]
    
    svg = f'''<svg xmlns="http://www.w3.org/2000/svg" width="600" height="400">
<defs>
<linearGradient id="bg" x1="0%" y1="0%" x2="100%" y2="100%">
<stop offset="0%" style="stop-color:{c1}"/>
<stop offset="50%" style="stop-color:{c2}"/>
<stop offset="100%" style="stop-color:{c3}"/>
</linearGradient>
</defs>
<rect width="600" height="400" fill="url(#bg)"/>
<text x="300" y="200" font-size="48" text-anchor="middle" dominant-baseline="central" fill="white" font-family="sans-serif">{ruin_name}</text>
</svg>'''
    
    b64 = base64.b64encode(svg.encode('utf-8')).decode('ascii')
    return f"data:image/svg+xml;base64,{b64}"

