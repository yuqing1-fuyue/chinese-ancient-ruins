@app.get("/api/search")
def search_ruins(keyword: str):
    results = []
    for city, ruins in city_ruins.items():
        for r in ruins:
            if (keyword in r["name"] or keyword in r["desc"]
                    or keyword in r["age"] or keyword in r["type"]
                    or keyword in r.get("era", "") or keyword in r.get("dynasty", "")):
                result = {**r, "city": city}
                result["image"] = get_image_url(r["name"])
                results.append(result)
    return {"results": results, "count": len(results)}

