@app.get("/api/ruins")
def get_ruins(city: str, era: str = ""):
    data = city_ruins.get(city, [])
    if era and era != "全部":
        data = [r for r in data if r.get("dynasty") == era or r.get("era") == era]
    # 注入图片URL
    for r in data:
        r["image"] = get_image_url(r["name"])
    return {"city": city, "ruins": data, "count": len(data)}

@app.get("/api/ruins_by_dynasty")
def get_ruins_by_dynasty(dynasty: str = ""):
    """按朝代获取全部遗址"""
    if not dynasty or dynasty == "全部":
        all_ruins = []
        for city, ruins in city_ruins.items():
            for r in ruins:
                ruin = {**r, "city": city}
                ruin["image"] = get_image_url(r["name"])
                all_ruins.append(ruin)
        return {"ruins": all_ruins, "count": len(all_ruins)}
    result = dynasty_ruins.get(dynasty, [])
    for r in result:
        r["image"] = get_image_url(r["name"])
    return {"dynasty": dynasty, "ruins": result, "count": len(result)}

@app.get("/api/dynasties")
def get_dynasties():
    """获取所有朝代列表（按时间顺序）"""
    available = list(dynasty_ruins.keys())
    ordered = [d for d in DYNASTY_ORDER if d in available]
    rest = [d for d in available if d not in DYNASTY_ORDER]
    return {"dynasties": ordered + rest, "counts": {d: len(dynasty_ruins[d]) for d in available}}

@app.get("/api/ruin_detail")
def get_ruin_detail(city: str, name: str):
    ruins = city_ruins.get(city, [])
    for r in ruins:
        if r["name"] == name:
            return {"ruin": r}
    return {"ruin": None, "msg": "未找到该遗址"}

